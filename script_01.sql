CREATE TABLE Office(ID NUMBER(5) PRIMARY KEY ,Office_NAME VARCHAR(50) , ADDRESS1 VARCHAR(100),
ADDRESS2 VARCHAR(100),CITY VARCHAR(50),STATE VARCHAR(50),PINCODE NUMBER(6)
);

CREATE TABLE Department(ID NUMBER(5) PRIMARY KEY,NAME VARCHAR(50));

CREATE TABLE Employees(ID NUMBER(10) PRIMARY KEY ,NAME VARCHAR(30),DOB DATE,OFFICE_ID
NUMBER(5), FOREIGN KEY (OFFICE_ID) REFERENCES OFFICE(ID) ,DEPT_ID
NUMBER(5),  FOREIGN KEY (DEPT_ID) REFERENCES Department(ID),
CONTACT_NO NUMBER(11),EMAIL VARCHAR(50),GENDER CHAR(1)
);

